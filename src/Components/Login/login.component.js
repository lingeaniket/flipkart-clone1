// TODO remove, this demo shouldn't need to reset the theme.

const defaultTheme = createTheme();

export default function SignIn() {
    const navigate = useNavigate();
    const isCheckOut = useSelector(state => state.orderDetailsState.checkout);
    const singleOrder = useSelector(state => state.orderDetailsState.singleOrder);
    const dispatch = useDispatch();
    const handleSubmit = (event) => {
        event.preventDefault();
        const data = new FormData(event.currentTarget);

        localStorage.setItem('isUserLoggedIn', true);
        dispatch(loginUser())
        localStorage.setItem('username', data.get('username'))
        if (singleOrder.length > 0) {
            navigate(`/checkout?item-id=${singleOrder[0].id}`)
        } else if (isCheckOut) {
            navigate('/checkout');
        } else {
            navigate('/');
        }
    };

    return (
        <ThemeProvider theme={defaultTheme}>
            <Container component="main" maxWidth="xs">
                <CssBaseline />
                <Box
                    sx={{
                        marginTop: 8,
                        display: 'flex',
                        flexDirection: 'column',
                        alignItems: 'center',
                    }}
                >
                    {isCheckOut
                        &&
                        <Typography component="h3" variant='p' sx={{ textAlign: 'center', fontSize: '14px', paddingBottom: '12px' }}>
                            Do not refresh this page as order checkout is in progress
                        </Typography>
                    }
                    <Avatar sx={{ m: 1, bgcolor: 'secondary.main' }}>
                        <LockOutlinedIcon />
                    </Avatar>
                    <Typography component="h1" variant="h5">
                        Sign in
                    </Typography>
                    <Box component="form" onSubmit={handleSubmit} noValidate sx={{ mt: 1 }}>
                        <TextField
                            margin="normal"
                            required
                            fullWidth
                            id="username"
                            label="User Name"
                            name="username"
                            autoFocus
                        />
                        <TextField
                            margin="normal"
                            required
                            fullWidth
                            name="password"
                            label="Password"
                            type="password"
                            id="password"
                        />
                        <Button
                            type="submit"
                            fullWidth
                            variant="contained"
                            sx={{ mt: 3, mb: 2 }}
                        >
                            Sign In
                        </Button>
                    </Box>
                </Box>
                <Copyright sx={{ mt: 8, mb: 4 }} />
            </Container>
        </ThemeProvider>
    );
}